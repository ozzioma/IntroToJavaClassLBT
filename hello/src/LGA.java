public class LGA
{
    private String lgaCode;
    public void setLGACode(String lga_Code)
    {
        this.lgaCode=lga_Code;
    }

    public String getLgaCode()
    {
        return lgaCode;
    }

    private String lgaName;
    public void setLGAName(String name)
    {
        this.lgaName=name;
    }

    public String getLgaName()
    {
        return lgaName;
    }

}
